      t.next_state <- Some sa.(n)
    
  let logprob t n_vis =
    let m = t.leds_on in
    if (m>=n_vis) then
      (float (m-n_vis)) *. logprob_on_but_cannot_see
    else
      (float (n_vis-m)) *. logprob_extra_led

  let logprob_stay t res n_vis =
    if res < t.max_residence then
      logprob t n_vis
    else
      logprob_zero

  let logprob_transition t nt res n_vis =
    let k = t.max_residence - res in
    logprob_early_transition *. (float k) +. (logprob nt n_vis)

  type tree = {
      history: (t * int) list;
      logprob: float;
      current : t;
      residence : int;
    }

  let new_tree t r = {
      history   = [];
      current   = t;
      residence = r;
      logprob   = 0.;
    }

  let next_tree_if_stays t n_vis addprob =
    {history = t.history;
     current = t.current;
     residence = t.residence+1;
     logprob   = addprob +. t.logprob +. logprob_stay t.current t.residence n_vis
    }

  let next_tree_if_transitions t n_vis addprob =
    let next_state = match t.current.next_state with Some s -> s | None -> t.current in
    {history = t.history @ [(t.current, t.residence)];
     current = next_state;
     residence = 1;
     logprob   = addprob +. t.logprob +. logprob_transition t.current next_state t.residence n_vis
    }

  let rec hist_string acc = function
      [] -> acc
     | (t,r)::tl ->
        let acc = Printf.sprintf "%s %d.%d" acc t.number r in
        hist_string acc tl
        
  let show_tree tree =
    Printf.printf "%d.%d : %f %s\n" tree.current.number tree.residence tree.logprob (hist_string "" tree.history)

end

let led_patterns = [
    ([], 8);
