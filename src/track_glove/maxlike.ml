 ]

let states =
  let sl = List.mapi (fun i (leds,res) -> State.create i leds res) led_patterns in
  let sa = Array.of_list sl in
  let ns = Array.length sa in
  Array.iteri (fun i t -> State.set_next_state t sa (i+1) ns) sa ;
  sa
  
let get_new_trees ct n_vis addprob =
  List.fold_left (fun acc tree -> (State.next_tree_if_stays tree n_vis addprob) ::
                                    (State.next_tree_if_transitions tree n_vis addprob) :: acc) [] ct
let filter_trees st minprob = List.filter (fun (t:State.tree) -> t.logprob > minprob) st

let best_prob st = List.fold_left (fun acc (tree:State.tree) -> let p = tree.logprob in if (p>acc) then p else acc) (-20.) st

let trees_agree_state_residence = function
    [] -> None
  | (tree:State.tree)::tl ->
         let rec find_if_match = function
             [] -> Some tree
           | (hd:State.tree) :: tl ->
              if ((hd.residence == tree.residence) && (hd.current == tree.current)) then
                find_if_match tl
              else
                None
         in
         find_if_match tl
         
